Okradanie i zaglądanie<br />
<br />
Okradanie i zaglądanie robi się bardzo trudno na początku jeżeli mamy po 25% umiejętności. Polecam na początek chociaz 40% okradania na manekinie zrobić.<br />
Potrzebujemy przedmiotu który da się łączyć i rozdzieląć, polecam bandaże lub złoto. Następnie kupujemy juczną lame i:<br />
Od 25% do 60% lecimy na skrypcie pod easyuo który znajdziesz poniżej.<br />
Od 60% do 100% jedziemy na jucznym koniu. Zaglądanie i Okradanie trenujemy jednocześnie.<br />
