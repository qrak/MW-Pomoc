Poradnik PvM<br /><br />
<br />
Jak wiadomo bylo duzo zmian z PvM ostatnimi czasu. Czesc z nich wprowadzila lekkie utrudnienia, ale nie az takie udupiajace. Nie bede opisywal pvm dla kazdej klasy z osobna, bo jak wiadomo solo malo rzeczy da sie zabic szczegolnie lucznikiem ;)<br />
<br />
Maly slowniczek dla nie ogarniajacych: <br />
ort/ortuje - rzuca czar Ort Sanct<br />
noxuje/podtruwa - rzuca czar In Nox (co powoduje, ze potworek sie na chwile zatrzymuje i leczy sie z trucizny)<br />
mobek - potworek<br />
<br />
Mag+mag:<br />
<br />
Prosta zasada - nie dac sie trafic mobkowi. Jeden mag rzuca Ort Sancty (Mana Vampire, 7 krag), a drugi In Noxy (Poison, 3 krag) - i tak na zmiane (jak mobek sie przezuci na ssajacego maga) i co pare chwil paralik, a wtedy obaj ssaja mane. Wiadomo, ze mana nie odzyskuje sie od tak, gdy jeden z magow jest bez many odbiega wymedytowac, a drugi podtruwa mobka czarem. Gdy mobek jest bez many, jeden z magow biega w koleczko (tak aby nie zostac trafionym przez moba, ale zeby drugi mag trafial czarami) i co jakis czas dossie mane, aby sie nie zaleczyl (najlepiej czarem Mana Drain, 4 krag) natomiast drugi pizga moba Corpami/Mind Blastami.<br />
<br />
Najlepiej zeby magowie byli ubrani jak najlepiej (secik mistyk, bron um, magiki do odpornosci na ogien, zimno, pioruny) i uzywali zoltych mikstur.<br />
<br />
Mag+woj:<br />
<br />
Najlepsze polaczenie jesli chodzi o wyjscie na PvM. Woj ubrany jak najlepiej (magiki do odpornosci na ogien, zimno, pioruny, bron um najlepiej trujaca). Woj klatuje, mag ortuje i daje prota. Jesli mobek przezuci sie na maga to ten jesli ma hida i widmo to wiadomo ukrywa sie, a jesli nie to rzuca parala lub noxa i odbiega tak, aby mobek przezucil sie na woja. Jesli mobowi skonczy sie mana - mag wali corpy/mind blasty i co jakis czas dosysa mane, aby ten sie nie zaleczyl.<br />
<br />
Jesli mag jest druidem-tamerem, to niech sie ograniczy do blessowania woja i rzucania klatw na mobka (bless natury, klatwa natury). Pod zadnym pozorem mag nie rzuca swojego smoka/chimy czy czegokolwiek innego bo wtedy mobek go sploszy i jest dodatkowy oponent ;p<br /><br />

